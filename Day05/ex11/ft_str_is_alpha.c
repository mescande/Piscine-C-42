int		ft_str_is_alpha(char *str)
{
	int i;
	int a;

	i = 0;
	a = 1;
	while (str[i] != 0 && a == 1)
	{
		if (str[i] > 'z' || str[i] < 'a')
		{
			if (str[i] > 'Z' || str[i] < 'A')
				a = 0;
		}
		i++;
	}
	return (a);
}
